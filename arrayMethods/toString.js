//toString() method joins the array and returns one string containing each array element separated by commas.
// returns an array as a string

let myname = [ 'j', 'e', 'a', 'n'];
let fullName = myname.toString('');
console.log(fullName); // j,e,a,n


const array1 = [1, 2, 'a', '1'];

console.log(array1.toString()); // 1,2,a,1
